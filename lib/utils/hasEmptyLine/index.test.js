import { expect, it } from "vitest"

import { hasEmptyLine } from "./index.js"

it(`hasEmptyLine`, () => {
	expect(hasEmptyLine(`\n\n`)).toBe(true)
	expect(hasEmptyLine(`\r\n\r\n`)).toBe(true)
	expect(hasEmptyLine(`\n\n\n\n`)).toBe(true)
	expect(hasEmptyLine(`\r\n\r\n\r\n\r\n`)).toBe(true)
	expect(hasEmptyLine(`   \n\n`)).toBe(true)
	expect(hasEmptyLine(`\n\n   \n\n`)).toBe(true)
	expect(hasEmptyLine(`\n \n`)).toBe(true)
	expect(hasEmptyLine(`\r\n \r\n`)).toBe(true)
	expect(hasEmptyLine(`\n \n \n \n`)).toBe(true)
	expect(hasEmptyLine(`\r \n\n r\n\r\n\r\n`)).toBe(true)
	expect(hasEmptyLine(`   \n \n`)).toBe(true)
	expect(hasEmptyLine(`\n \n   \n \n`)).toBe(true)
	expect(hasEmptyLine(`\n\t\n`)).toBe(true)
	expect(hasEmptyLine(`\n\t \n`)).toBe(true)
	expect(hasEmptyLine(`\r\n\t\r\n`)).toBe(true)
	expect(hasEmptyLine(`\n\t\n\n \t\n`)).toBe(true)
	expect(hasEmptyLine(`\r\n\t\r\n\t\r\n\r\n`)).toBe(true)
	expect(hasEmptyLine(`   \n\t\n`)).toBe(true)
	expect(hasEmptyLine(`\n\t\n  \t  \n\n`)).toBe(true)
	expect(hasEmptyLine(``)).toBe(false)
	expect(hasEmptyLine(` `)).toBe(false)
	expect(hasEmptyLine(`\t`)).toBe(false)
	expect(hasEmptyLine(`\n`)).toBe(false)
	expect(hasEmptyLine(`\r\n`)).toBe(false)
})
